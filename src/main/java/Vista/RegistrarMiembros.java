    private void limpiar() {
        this.textnombre.setText("");
        this.textapellido.setText("");
        this.cbxtipo.setSelectedIndex(0);
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try {
            
            String nombre = this.textnombre.getText();
            String apellido = this.textapellido.getText();
            String tipo = String.valueOf(this.cbxtipo.getSelectedItem());

            Miembro objMiembro = new Miembro(nombre,apellido,tipo);
            
            
            if (this.objRegistroMiembro.agregar(objMiembro)) {
                JOptionPane.showMessageDialog(this, "Miembro registrado con Ã©xito!");
                Eleccion actual = objRegistroEleccion.getListaEleccion()[indexfecha];
                Mesa mesaSeleccionada = actual.getMesas()[indexmesa];
            
            
                mesaSeleccionada.agregarMiembro(objMiembro); 
                
            } else {
                JOptionPane.showMessageDialog(this, "Error en el registro!");
            }
            this.limpiar();
        } catch (Exception e) {
            e.printStackTrace(); // Para ver el error exacto en la consola
            JOptionPane.showMessageDialog(this, "Error en ingreso de datos: " + e.getMessage(), "Datos ingresados en el formulario no son correctos.", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

