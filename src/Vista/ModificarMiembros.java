    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       try {
        String nombre = this.txtNombre.getText();
        String apellido = this.txtapellido.getText();
        String tipo = String.valueOf(this.cbxmiembro.getSelectedItem());

        Miembro objMiembro = new Miembro(nombre, apellido, tipo);


        if (this.objRegistroEleccion.ModificarMiembro(indexfecha, indexmesa, indexmiembro, objMiembro)) {
            JOptionPane.showMessageDialog(this, "Miembro modificado con Ã©xito!");
            objGestionarMiembros.moverArregloATabla();
        } else {
            JOptionPane.showMessageDialog(this, "Error en el registro!");
        }
        this.limpiar();
    } catch (Exception e) {
        
        JOptionPane.showMessageDialog(this, "Error en ingreso de datos: " + e.getMessage(), "Datos ingresados en el formulario no son correctos.", JOptionPane.ERROR_MESSAGE);
        
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton4ActionPerformed

