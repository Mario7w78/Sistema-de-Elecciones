     public void limpiarTabla() {
        model1 = new DefaultTableModel();
        this.tblMesas.setModel(model1);

        for (String column : COLUMNS) {
           model1.addColumn(column); 
        }
    }
        public void moverArregloATabla() {
        this.limpiarTabla();
        int count = 0;
        if(this.cbxfecha.getSelectedIndex()!= -1){
        Mesa[] listaMesa = this.objRegistroEleccion.getMesasDeVotacion(this.cbxfecha.getSelectedIndex());
        if (listaMesa!= null){
        String[] fila = new String[COLUMNS.length];

        for (int i = 0; i < listaMesa.length; i++) {
            Mesa mesa = listaMesa[i];

            
            if (mesa != null) {  
        fila[0] = mesa.getNmesa();
        fila[1] = mesa.getUbicacion();
        fila[2] = String.valueOf(mesa.getNum());
        count++;
        model1.addRow(fila);
    }
        }
        
        
        }
    }}
