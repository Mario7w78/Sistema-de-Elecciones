    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try {
            
            String ubicacion = this.txtubicacion.getText();
            String numMesa = this.txtmesa.getText();           

            Mesa objMesa = new Mesa(ubicacion, numMesa);
            
            
            if (this.objRegistroEleccion.ModificarMesa(indexfecha,indexmesa, objMesa)) {
                
                JOptionPane.showMessageDialog(this, " Mesa modificada con éxito!");
                objGestionarMesa.moverArregloATabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error en el registro!");
            }
            this.limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en ingreso de datos de " , "Datos ingresados en el formulario no son correctos.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

