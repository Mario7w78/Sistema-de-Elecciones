    public ModificarMesa(RegistroEleccion objregistroeleccion, int indexfecha, int indexmesa, GestionarMesa objGestionarMesa) {
    initComponents();
    this.objRegistroEleccion = objregistroeleccion;
    this.indexfecha = indexfecha;
    this.indexmesa = indexmesa;
    this.objGestionarMesa = objGestionarMesa;
    
    Eleccion[] lista = objRegistroEleccion.getListaEleccion();
    Eleccion actualelec = lista[indexfecha];
    Mesa[] mesas = actualelec.getMesas();
    Mesa actualmesa = mesas[indexmesa];
    if (actualmesa != null) {
        
        Miembro[] miembrosExistentes = actualmesa.getMiembros();
        int numMiembrosExistentes = actualmesa.getNum();
        this.txtactualubicacion.setText(actualmesa.getUbicacion());
        this.txtnummesaactual.setText(actualmesa.getNmesa());
        
        } else {
        
        this.txtactualubicacion.setText("");
        this.txtnummesaactual.setText("");
        
        }
}

